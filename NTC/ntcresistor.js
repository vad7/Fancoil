function getNum(elemId, lang) {
  var elem = document.getElementById(elemId);
  var val = elem.value;
  if ((val == '') || (val == null) || !/^\-?([0-9]+(\.[0-9]+)?)$/.test(val)) {
    elem.focus();
    window.alert(((lang == 'ru') ? 'Ошибка в числе' : 'Wrong number') + ': ' + val);
    return null;
  }
  return parseFloat(val);
}

