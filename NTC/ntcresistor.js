function ntcSplitVals(v) {
  var ar = v.toString().split(',');
  var res = new Array();
  for (var i = 0; i < ar.length; i++) { 
    var v = parseFloat(ar[i].trim());
    if (isNaN(v)) 
      return null;
    res[i] = v;
  }
  return res;
}

